package y2023

fun main() {
    fun pivot(matrix: List<String>) =
        (0 until matrix[0].length).map { x ->
            (matrix.indices).map { y ->
                if (x < matrix[y].length) matrix[y][x] else "*"
            }.joinToString("")
        }

    println(
        pivot(
            pivot(flip.split("\n")).map { row ->
                row.split(Regex("(?<=[#])|(?=[#])")).map { splitted ->
                    if (splitted.contains(".") || splitted.contains("O")) {
                        splitted.filter { it == 'O' } + splitted.filter { it == '.' }
                    } else {
                        if (splitted == "#") "#" else ""
                    }
                }.joinToString("")
            }
        ).reversed().mapIndexed { index, s ->
            s.filter { it == 'O' }.length * (index + 1)
        }.sum()
    )

}

